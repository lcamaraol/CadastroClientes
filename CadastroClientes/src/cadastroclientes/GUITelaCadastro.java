    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String nome = jTextField1.getText();
    String telefoneStr = jTextField2.getText();
    String email = jTextField3.getText();
    String logradouro = jTextField4.getText();
    String numeroStr = jTextField5.getText();
    String complemento = jTextField6.getText();
    String bairro = jTextField7.getText();
    String cidade = jTextField8.getText();
    String estado = jTextField9.getText();
    String cep = jTextField10.getText();

    // Verificar se campos obrigatÃ³rios estÃ£o preenchidos
    if (nome.isEmpty() || telefoneStr.isEmpty() || email.isEmpty() || logradouro.isEmpty() ||
        numeroStr.isEmpty() || bairro.isEmpty() || cidade.isEmpty() || estado.isEmpty() || cep.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos os campos obrigatÃ³rios devem ser preenchidos.", "Erro de ValidaÃ§Ã£o", JOptionPane.ERROR_MESSAGE);
        return; // Sai do mÃ©todo para evitar o cadastro com campos faltando
    }

    // Verificar se o telefone Ã© um nÃºmero vÃ¡lido
    int telefone;
    try {
        telefone = Integer.parseInt(telefoneStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "O telefone deve ser um nÃºmero vÃ¡lido.", "Erro de ValidaÃ§Ã£o", JOptionPane.ERROR_MESSAGE);
        return; // Sai do mÃ©todo se o telefone nÃ£o for um nÃºmero vÃ¡lido
    }

    // Verificar se o nÃºmero Ã© um nÃºmero vÃ¡lido
    int numero;
    try {
        numero = Integer.parseInt(numeroStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "O nÃºmero deve ser um nÃºmero vÃ¡lido.", "Erro de ValidaÃ§Ã£o", JOptionPane.ERROR_MESSAGE);
        return; // Sai do mÃ©todo se o nÃºmero nÃ£o for um nÃºmero vÃ¡lido
    }

    // Criar um objeto Cliente com os valores
    Cliente novoCliente = new Cliente(nome, telefone, email, logradouro, numero, complemento, bairro, cidade, estado, cep);
    
    
    // Adicionar o novo cliente Ã  lista de clientes (ou armazenar em um banco de dados)
    CadastroClientes.adicionarCliente(novoCliente);
    // Exibir uma mensagem de sucesso
    JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");

    // Limpar os campos apÃ³s o cadastro (opcional)
    limparCampos();
    }//GEN-LAST:event_jButton1ActionPerformed

